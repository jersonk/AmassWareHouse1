package com.amassfreight.domain;

public class DepotVas {

	public DepotVas(){
		
	}
	private String serviceId;
	private String depotDtId;
	private String flgService;

	public String getServiceId() {
		return serviceId;
	}
	public void setServiceId(String serviceId) {
		this.serviceId = serviceId;
	}
	public String getDepotDtId() {
		return depotDtId;
	}
	public void setDepotDtId(String depotDtId) {
		this.depotDtId = depotDtId;
	}
	public String getFlgService() {
		return flgService;
	}
	public void setFlgService(String flgService) {
		this.flgService = flgService;
	}
	
}
